public  class Bread{

	private int width;
	private int length;

	public Bread(){


}

	public Bread(int width){
		this.width = width;

}

	public Bread(int width, int length){
	this.width = width;
	this.length = length;

}


	public int getWidth(){

		return width;
}

	
	public int getLength(){

		return length;
}





}
